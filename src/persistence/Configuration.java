package persistence;

public class Configuration {
    private String user;
    private String password;
    private int port;
    private String server;

    public String getUser() {
        return user;
    }

    public String getPassword() {
        return password;
    }

    public int getPort() {
        return port;
    }

    public String getServer() {
        return server;
    }
}
